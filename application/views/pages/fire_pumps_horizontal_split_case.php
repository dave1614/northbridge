									<div class="row">

										<div class="col-md-6 col-xs-12">

											<p><strong>Casing</strong> - (Ductile Iron) - Upper and Lower half bolted and dowelled to provide perfect alignment. Upper half casing can be removed for inspection, without disturbing bearings or alignment.</p>

											<p><strong>Shaft Sleeve</strong> - (Stainless Steel) - Protect the shaft against corrosion and wear, extends through gland for maximum shaft protection.</p>

											<p><strong>Impeller</strong> - (Bronze) - Enclosed, double suction, Cast in one-piece and balance to minimize the thrust and to ensure longer bearing life. Locked in position by shaft sleeve.</p>

											<p><strong>Sealing Housing</strong> - (Ductile Iron) - Designed to accept packing with lantern ring. Internally drilled liquid passage in upper-half casing provides lubrication to the packing area.</p>

											<p><strong>Shaft</strong> - (Steel) - Large - diameter, precision-machined, high strength steel shaft for maximum strength with minimum shaft deflection.</p>

											<p><strong>Bearing</strong> - (SKF) - High speed capability and low friction bearing.</p>

											<p><strong>Wear Ring</strong> - (Bronze) - A sacrificial component installed to inhibit fluid from re-circulating back to suction from the discharge.</p>

											<p><strong>Lantern Ring</strong> - (Stainless Steel) - A perforated hollow ring that receives relatively cool, clean liquid. Distribute uniformly around the shaft to provide lubrication and cooling.</p>

											<p><strong>Packing Gland</strong> - (Immersion Graphite) - flexibility allows the shaft to run freely as well as leak proof.</p>

											<p><strong>Stuffing Box (Gland)</strong> - (Bronze) - to press the packing gland into the seal area through bolts.</p>

											<p><strong>Plug</strong> - (Stainless Steel).</p>

											<p><strong>Bearing Housing</strong> - (Cast Iron).</p>

											<p><strong>Sealing Flush Line</strong> - (Stainless Steel) - Line from discharge of the pump and recirculated through the pressure regulating valve into the gland.</p>

											<p><strong>Water Slinger</strong> - (Oil Resistant Rubber).</p>

											<p><strong>Pump Coupling</strong> (ASTM No.35).</p>

