							<h4>Hydraulic Rising Kerb Road Blocker</h4>
							<p> The hydraulic road blocker is a vehicle control device that can be either integrated with various parking systems or installed as single unit, designed as an effective means of controlling access to high security areas such as airports, ports, customs, governmental buildings, banks, penitentiaries, power stations, military sites, stores, embassies, warehouses. The hydraulic road blocker is designed to guarantee the full level of security. The blocker is hidden, buried or embedded in the ground at the entrance of the security gateway of a protected site and are erected automatically under alarm conditions, such as when entrance of a suspected vehicle is to be prevented. Under normal conditions, road blockers remain buried under the ground allowing vehicular traffic flow to the site. Erection and retraction of road blockers are usually made by hydraulic actuating systems.</p>

