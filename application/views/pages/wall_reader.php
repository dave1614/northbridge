							<p>Wall Reader Access Control system is mainly used to give access to a certain room, office or building (depending on your application) to certain people. With the outdoor slim keypad, the VIS-3003, you can gain access through three different options. You can enter a pin code onto the keypad, use a proximity card, and/or use a proximity key tag. This keypad will allow up to 2000 users through either of the three access options. Since this is a standalone system, meaning that there is no software, it is recommended for you to keep a record of all personnel and their assigned user ID with key code, key card or key tag. On the keypad, you will notice a doorbell towards the bottom. This will allow people from the outside to ring the doorbell and notify those inside that there is someone wanting to come in. Once someone has entered a programmed code, card, or tag, the maglock will disengage and allow for entrance. The lock will then engage again and remain closed until it is unlocked by one of the three access options. The VIS-7005 exit button will allow those from within the facility to exit when they push the button. Through the outdoor keypad, you can program your delay from 0 to 99 seconds. The meaning of having a delay is setting a time until the maglock engages again. The popular applications for this kit is for offices, commercial buildings or any room that needs restricted access..</p>

